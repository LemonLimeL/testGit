DROP TABLE EMPLOYEE
CASCADE CONSTRAINTS; 

CREATE TABLE EMPLOYEE(
	EMPID NUMBER CONSTRAINT EMP_PRI PRIMARY KEY,
	NAME VARCHAR2(10) NOT NULL,
	JOBID VARCHAR2(30) NOT NULL,
	PHONE VARCHAR2(50),
	HIREDATE DATE NOT NULL
)

CREATE SEQUENCE EMP_SEQ
INCREMENT BY 1
START WITH 100

